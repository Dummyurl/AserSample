public class EnglishProficiency {
    private String englishProficiency;
    private String capitalLetter_mistake;
    private String smallLetter_mistake;
    private String words_mistake;
    private String sentence_mistake;

    public String getEnglishProficiency() {
        return englishProficiency;
    }

    public void setEnglishProficiency(String englishProficiency) {
        this.englishProficiency = englishProficiency;
    }

    public String getCapitalLetter_mistake() {
        return capitalLetter_mistake;
    }

    public void setCapitalLetter_mistake(String capitalLetter_mistake) {
        this.capitalLetter_mistake = capitalLetter_mistake;
    }

    public String getSmallLetter_mistake() {
        return smallLetter_mistake;
    }

    public void setSmallLetter_mistake(String smallLetter_mistake) {
        this.smallLetter_mistake = smallLetter_mistake;
    }

    public String getWords_mistake() {
        return words_mistake;
    }

    public void setWords_mistake(String words_mistake) {
        this.words_mistake = words_mistake;
    }

    public String getSentence_mistake() {
        return sentence_mistake;
    }

    public void setSentence_mistake(String sentence_mistake) {
        this.sentence_mistake = sentence_mistake;
    }
}
